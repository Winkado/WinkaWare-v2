local function PNNL_fake_script() -- ScreenGui.Keybind 
	local script = Instance.new('LocalScript', ScreenGui)

	local main = script.parent.main
	local opened = false
	
	local PositionClosed = UDim2.new(-0.211, 0,0.754, 0)
	local PositionOpened = UDim2.new(0.778, 0,0.769, 0)
	
	local UserInputService = game:GetService("UserInputService")
	
	UserInputService.InputBegan:Connect(function(KeyCode)
		if KeyCode.KeyCode == Enum.KeyCode.RightShift then
			if opened then
				main:TweenPosition((PositionClosed), "InOut", "Quint")
				opened=false
			else
				main:TweenPosition((PositionOpened), "InOut", "Quart")
				opened = true
			end
		end
	end)
end
